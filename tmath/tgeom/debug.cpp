const int   g_dbgBufferSize = 2048;
char        g_dbgBuffer[g_dbgBufferSize];
bool        g_bDebugSpew = false;

//-----------------------------------------------------------------------------
// functions
//-----------------------------------------------------------------------------
void
DebugPrint (const char* format, ...)
{
    va_list argList;
    va_start (argList, format);
    vsprintf_s (g_dbgBuffer, g_dbgBufferSize, format, argList);
    OutputDebugString (g_dbgBuffer);
    va_end (argList);
}

