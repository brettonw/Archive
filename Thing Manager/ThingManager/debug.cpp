#include    "precompile.h"
#include    "debug.h"

//-----------------------------------------------------------------------------
// global variables
//-----------------------------------------------------------------------------
char            g_dbgBuffer[2048];

//-----------------------------------------------------------------------------
// functions
//-----------------------------------------------------------------------------
void
DebugPrint (const char* format, ...)
{
    va_list argList;
    va_start (argList, format);
    vsprintf (g_dbgBuffer, format, argList);
    OutputDebugString (g_dbgBuffer);
    va_end (argList);
}

