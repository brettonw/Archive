#ifndef		_BW_HASH_H_
#define		_BW_HASH_H_

//--------------------------------------------------------------------------
//	function declarations
//--------------------------------------------------------------------------
inline
uInt	Hash (uInt value)
{
	return value;
}

//--------------------------------------------------------------------------
inline
uInt	Hash (sInt value)
{
	return static_cast<uInt> (value);
}

//--------------------------------------------------------------------------
inline
uInt	Hash (void* ptr)
{
	return reinterpret_cast<uInt> (ptr);
}

//--------------------------------------------------------------------------
inline
uInt	Hash (cString text)
{
	uInt	value = 0;
	while (*text)
		value = (value << 1) xor *text++;
	return value;
}

//--------------------------------------------------------------------------

#endif	//	_BW_HASH_H_
