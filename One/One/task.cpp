Task::Task
(
	const Text&		name
)
{
	myName = name;
	TaskRegistrar::RegisterTask (this);
}

//--------------------------------------------------------------------------
Task::~Task
(
	void
)
{
	//	destruction is in random order, do something in the destructor
	//	only if it does not depend on anything else
}

//--------------------------------------------------------------------------
void
Task::Startup
(
	void
) const
{
}

//--------------------------------------------------------------------------
void
Task::Shutdown
(
	void
) const
{
}

//--------------------------------------------------------------------------
const Text&
Task::GetName
(
	void
) const
{
	return myName;
}

//--------------------------------------------------------------------------
void
Task::DoAfter
(
    GraphOf<Task*>* graph, 
    const Text&     name
) const
{
    graph->AddEdge (name, GetName ());
}

