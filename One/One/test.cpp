#include	"precompile.h"
#include	"test_registrar.h"

//--------------------------------------------------------------------------
//	class methods
//--------------------------------------------------------------------------
Test::Test
(
	const Text&		name
)
{
	myName = name;
	TestRegistrar::RegisterTest (this);
}

//--------------------------------------------------------------------------
Test::~Test
(
	void
)
{
}

//--------------------------------------------------------------------------
const Text&
Test::GetName
(
	void
) const
{
	return myName;
}

//--------------------------------------------------------------------------
void
Test::DoAfter
(
    GraphOf<Test*>* graph, 
    const Text&     name
) const
{
    graph->AddEdge (name, GetName ());
}

